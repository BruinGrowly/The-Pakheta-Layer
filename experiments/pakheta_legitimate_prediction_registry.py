def build_branch_address_registry(transition):
    compression = []
    for row in transition["compression_transitions"]["transitions"]:
        compression.append({
            "id": f"CP-{row['operator'].upper()}",
            "branch": "inverse_operator_compression",
            "operator": row["operator"],
            "relation_type": row["relation_type"],
            "mass_MeV": row["mass_MeV"],
            "mass_GeV": row["mass_GeV"],
            "physical_regime": row["physical_regime"],
            "prediction_statement": (
                "Follow-up low-energy threshold or resonance structure should show an interpretable "
                f"{row['relation_type']} address near {row['mass_MeV']:.6f} MeV."
            ),
            "registration_windows": {
                "one_percent_MeV": [
                    row["mass_MeV"] * 0.99,
                    row["mass_MeV"] * 1.01,
                ],
                "five_percent_MeV": [
                    row["mass_MeV"] * 0.95,
                    row["mass_MeV"] * 1.05,
                ],
            },
        })

    amplification = []
    for row in transition["amplification_transitions"]["transitions"]:
        amplification.append({
            "id": f"AP-{row['operator'].upper()}",
            "branch": "direct_operator_amplification",
            "operator": row["operator"],
            "relation_type": row["relation_type"],
            "mass_MeV": row["mass_MeV"],
            "mass_GeV": row["mass_GeV"],
            "physical_regime": row["physical_regime"],
            "prediction_statement": (
                "Follow-up multi-GeV resonance or threshold structure should show an interpretable "
                f"{row['relation_type']} address near {row['mass_GeV']:.6f} GeV."
            ),
            "registration_windows": {
                "one_percent_GeV": [
                    row["mass_GeV"] * 0.99,
                    row["mass_GeV"] * 1.01,
                ],
                "five_percent_GeV": [
                    row["mass_GeV"] * 0.95,
                    row["mass_GeV"] * 1.05,
                ],
            },
        })

    return {
        "compression_addresses": compression,
        "amplification_addresses": amplification,
    }


def rank_light_predictions(light_predictions):
    ranked = []
    for prediction in light_predictions["predictions"]:
        overlay = prediction["known_overlay"]
        if "absolute_delta_MeV" in overlay:
            ranked.append({
                "id": prediction["id"],
                "name": prediction["name"],
                "absolute_delta_MeV": overlay["absolute_delta_MeV"],
                "absolute_relative_delta": overlay["absolute_relative_delta"],
            })
        elif "against_a3_mean" in overlay:
            ranked.append({
                "id": prediction["id"],
                "name": prediction["name"],
                "absolute_delta_MeV": overlay["against_a3_mean"]["absolute_delta_MeV"],
                "absolute_relative_delta": overlay["against_a3_mean"]["absolute_relative_delta"],
            })
    ranked.sort(key=lambda row: row["absolute_relative_delta"])
    return ranked


