"""
Pakheta Legitimate Prediction Registry.

This script converts the relationship-first transition atlas into explicit,
fixed-grammar predictions. The rules are declared before the known-value overlay:

1. The cosmological mass anchor opens a deuteron binding gate.
2. The three-body light-nuclear boundary is generated by a Justice step.
3. The closed alpha relation is generated by summed Justice + Love binding
   channels over the same gate.
4. The inverse and direct mass branches remain registered as threshold
   addresses for follow-up comparison.
"""

import json
import math
from datetime import datetime, timezone
from pathlib import Path


EXPERIMENT_DIR = Path(__file__).resolve().parent
TRANSITION_FILE = EXPERIMENT_DIR / "transition_atlas_results.json"
OUTPUT_FILE = EXPERIMENT_DIR / "legitimate_prediction_registry_results.json"

BASE = 30.0
L0 = (math.sqrt(5.0) - 1.0) / 2.0
J0 = math.sqrt(2.0) - 1.0
P0 = math.e - 2.0
W0 = math.log(2.0)

OPERATORS = {
    "Love": L0,
    "Justice": J0,
    "Power": P0,
    "Wisdom": W0,
}

# NIST CODATA 2022 values already used elsewhere in the repo.
LIGHT_NUCLEAR_VALUES = {
    "proton_mass_MeV": 938.27208943,
    "neutron_mass_MeV": 939.56542194,
    "deuteron_mass_MeV": 1875.61294500,
    "helion_mass_MeV": 2808.39161112,
    "triton_mass_MeV": 2808.92113668,
    "alpha_particle_mass_MeV": 3727.3794118,
}


def read_transition_atlas():
    with open(TRANSITION_FILE, "r", encoding="utf-8") as file:
        return json.load(file)


def operator_factors():
    return {
        name: BASE ** value
        for name, value in OPERATORS.items()
    }


def relative_delta(predicted, observed):
    return predicted / observed - 1.0


def residual_row(predicted, observed):
    delta = predicted - observed
    return {
        "predicted_MeV": predicted,
        "observed_MeV": observed,
        "delta_MeV": delta,
        "absolute_delta_MeV": abs(delta),
        "relative_delta": relative_delta(predicted, observed),
        "absolute_relative_delta": abs(relative_delta(predicted, observed)),
    }


def light_nuclear_observations():
    proton = LIGHT_NUCLEAR_VALUES["proton_mass_MeV"]
    neutron = LIGHT_NUCLEAR_VALUES["neutron_mass_MeV"]
    deuteron = LIGHT_NUCLEAR_VALUES["deuteron_mass_MeV"]
    helion = LIGHT_NUCLEAR_VALUES["helion_mass_MeV"]
    triton = LIGHT_NUCLEAR_VALUES["triton_mass_MeV"]
    alpha = LIGHT_NUCLEAR_VALUES["alpha_particle_mass_MeV"]

    deuteron_binding = proton + neutron - deuteron
    helion_binding = 2.0 * proton + neutron - helion
    triton_binding = proton + 2.0 * neutron - triton
    alpha_binding = 2.0 * proton + 2.0 * neutron - alpha

    return {
        "deuteron_total_binding_MeV": deuteron_binding,
        "helion_total_binding_MeV": helion_binding,
        "triton_total_binding_MeV": triton_binding,
        "a3_mean_total_binding_MeV": (helion_binding + triton_binding) / 2.0,
        "a3_min_total_binding_MeV": min(helion_binding, triton_binding),
        "a3_max_total_binding_MeV": max(helion_binding, triton_binding),
        "a3_isospin_split_MeV": triton_binding - helion_binding,
        "alpha_total_binding_MeV": alpha_binding,
        "alpha_per_nucleon_binding_MeV": alpha_binding / 4.0,
        "alpha_to_deuteron_binding_ratio": alpha_binding / deuteron_binding,
        "a3_mean_to_deuteron_binding_ratio": ((helion_binding + triton_binding) / 2.0) / deuteron_binding,
    }


def binding_gate_from_transition(transition):
    chain = transition["binding_transitions"]["chain"]
    for step in chain:
        if step["name"] == "mass_anchor_to_one_u_offset":
            return step["value_MeV"]
    raise KeyError("mass_anchor_to_one_u_offset not found in transition atlas")


