    transition = read_transition_atlas()
    factors = operator_factors()
    observations = light_nuclear_observations()
    binding_gate = binding_gate_from_transition(transition)

    light_predictions = build_light_nuclear_predictions(binding_gate, factors, observations)
    branch_addresses = build_branch_address_registry(transition)
    ranked_light_predictions = rank_light_predictions(light_predictions)

